
public class Condition {

	public static void main(String[] args) {
		
		for (int i = 1; i <= 3 ;i++)
		{
			for (int j =1; j<=i; j++)
			{
			System.out.print('*'); // 여러번
			}
			System.out.println();
		}
		
		for (int i = 2; i > 0 ;i--)
		{
			for (int j =1; j<=i; j++)
			{
			System.out.print('*'); // 여러번
			}
			System.out.println();
		}
		
		return;
	}
}
